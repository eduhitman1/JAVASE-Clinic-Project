    public void  preencharMedicos(){
        conex.conexao();
        conex.executaSql("select *from medicos order by nome_medico");
        try {
            conex.rs.first();
            jComboBoxMedicos.removeAllItems();
            do{
            jComboBoxMedicos.addItem(conex.rs.getString("nome_medico"));        
            }while(conex.rs.next());
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(this,"Erro ao preencher Bairros\n Erro: "+ex);
        }
        conex.desconecta();
    }
    
    
    

