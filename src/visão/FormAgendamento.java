        public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();   // instancia da arraylist
        String [] colunas = new String[]{"ID","Nome","RG","Telefone","Bairro"};   // recebe o nomes das colunas
        conex.conexao();
        conex.executaSql(Sql);
        try{
        conex.rs.first();
        do{
            dados.add(new Object[]{conex.rs.getInt("paci_codigo"),
                                   conex.rs.getString("paci_nome"),
                                   conex.rs.getString("paci_rg"),
                                   conex.rs.getString("paci_telefone"),
                                   conex.rs.getString("bainome")});  // variavel de outra tabela
        }while(conex.rs.next());  // quanto houve dados na base será exibido
        }catch(SQLException ex){
        JOptionPane.showMessageDialog(rootPane,"Busque por outro médico");
        }
            ModeloTabela modelo = new ModeloTabela(dados, colunas);  // quantidade de linhas e colunas
        jTablePaciente.setModel(modelo);                                 // passando modelo da tabela
        jTablePaciente.getColumnModel().getColumn(0).setPreferredWidth(23);   //tamanha da tabela, começa do zero, espaço da coluna
        jTablePaciente.getColumnModel().getColumn(0).setResizable(false);     // desabilidado para redimensionar
        jTablePaciente.getColumnModel().getColumn(1).setPreferredWidth(180);
        jTablePaciente.getColumnModel().getColumn(1).setResizable(false);
        jTablePaciente.getColumnModel().getColumn(2).setPreferredWidth(180);
        jTablePaciente .getColumnModel().getColumn(2).setResizable(false);
        jTablePaciente.getColumnModel().getColumn(3).setPreferredWidth(150);
        jTablePaciente.getColumnModel().getColumn(3).setResizable(false);
        jTablePaciente.getColumnModel().getColumn(4).setPreferredWidth(150);
        jTablePaciente.getColumnModel().getColumn(4).setResizable(false);
        
        jTablePaciente.getTableHeader().setReorderingAllowed(false); // desabilitado para reodenar
        jTablePaciente.setAutoResizeMode(jTablePaciente.AUTO_RESIZE_OFF); // desabilidata para redimensionar a tabela
        jTablePaciente.setSelectionMode(ListSelectionModel.SINGLE_SELECTION); // apenas seleciona um dado por vez
        conex.desconecta();
   
    
    
    
    
    }

    
    
